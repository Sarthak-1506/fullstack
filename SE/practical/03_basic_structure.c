#include <stdio.h>

int main() {
    // variables
    int age = 20;
    float height = 5.8;
    char grade = 'A';

    // constant
    const int year = 2026;

    printf("Age: %d\n", age);
    printf("Height: %.2f\n", height);
    printf("Grade: %c\n", grade);
    printf("Year: %d\n", year);

    return 0;
}
